vec4 dissolve_mask(vec4 tex, vec2 texture_coords, vec2 uv)
{
    if (dissolve < 0.001) {
        return vec4(shadow ? vec3(0.,0.,0.) : tex.xyz, shadow ? tex.a*0.3: tex.a);
    }

    float adjusted_dissolve = (dissolve*dissolve*(3.-2.*dissolve))*1.02 - 0.01; //Adjusting 0.0-1.0 to fall to -0.1 - 1.1 scale so the mask does not pause at extreme values

	float t = time * 10.0 + 2003.;
	vec2 floored_uv = (floor((uv*texture_details.ba)))/max(texture_details.b, texture_details.a);
    vec2 uv_scaled_centered = (floored_uv - 0.5) * 2.3 * max(texture_details.b, texture_details.a);
	
	vec2 field_part1 = uv_scaled_centered + 50.*vec2(sin(-t / 143.6340), cos(-t / 99.4324));
	vec2 field_part2 = uv_scaled_centered + 50.*vec2(cos( t / 53.1532),  cos( t / 61.4532));
	vec2 field_part3 = uv_scaled_centered + 50.*vec2(sin(-t / 87.53218), sin(-t / 49.0000));

    float field = (1.+ (
        cos(length(field_part1) / 19.483) + sin(length(field_part2) / 33.155) * cos(field_part2.y / 15.73) +
        cos(length(field_part3) / 27.193) * sin(field_part3.x / 21.92) ))/2.;
    vec2 borders = vec2(0.2, 0.8);

    float res = (.5 + .5* cos( (adjusted_dissolve) / 82.612 + ( field + -.5 ) *3.14))
    - (floored_uv.x > borders.y ? (floored_uv.x - borders.y)*(5. + 5.*dissolve) : 0.)*(dissolve)
    - (floored_uv.y > borders.y ? (floored_uv.y - borders.y)*(5. + 5.*dissolve) : 0.)*(dissolve)
    - (floored_uv.x < borders.x ? (borders.x - floored_uv.x)*(5. + 5.*dissolve) : 0.)*(dissolve)
    - (floored_uv.y < borders.x ? (borders.x - floored_uv.y)*(5. + 5.*dissolve) : 0.)*(dissolve);

    if (tex.a > 0.01 && burn_colour_1.a > 0.01 && !shadow && res < adjusted_dissolve + 0.8*(0.5-abs(adjusted_dissolve-0.5)) && res > adjusted_dissolve) {
        if (!shadow && res < adjusted_dissolve + 0.5*(0.5-abs(adjusted_dissolve-0.5)) && res > adjusted_dissolve) {
            tex.rgba = burn_colour_1.rgba;
        } else if (burn_colour_2.a > 0.01) {
            tex.rgba = burn_colour_2.rgba;
        }
    }

    return vec4(shadow ? vec3(0.,0.,0.) : tex.xyz, res > adjusted_dissolve ? (shadow ? tex.a*0.3: tex.a) : .0);
}

vec3 linear_srgb_to_oklab(vec3 c) 
{
    float l = 0.4122214708f * c.r + 0.5363325363f * c.g + 0.0514459929f * c.b;
	float m = 0.2119034982f * c.r + 0.6806995451f * c.g + 0.1073969566f * c.b;
	float s = 0.0883024619f * c.r + 0.2817188376f * c.g + 0.6299787005f * c.b;

    float l_ = pow(l, 1.0/3.0);
    float m_ = pow(m, 1.0/3.0);
    float s_ = pow(s, 1.0/3.0);

    return vec3(
        0.2104542553f*l_ + 0.7936177850f*m_ - 0.0040720468f*s_,
        1.9779984951f*l_ - 2.4285922050f*m_ + 0.4505937099f*s_,
        0.0259040371f*l_ + 0.7827717662f*m_ - 0.8086757660f*s_
    );
}

vec3 oklab_to_linear_srgb(vec3 c) 
{
    float l_ = c.r + 0.3963377774f * c.g + 0.2158037573f * c.b;
    float m_ = c.r - 0.1055613458f * c.g - 0.0638541728f * c.b;
    float s_ = c.r - 0.0894841775f * c.g - 1.2914855480f * c.b;

    float l = l_*l_*l_;
    float m = m_*m_*m_;
    float s = s_*s_*s_;

    return vec3(
		+4.0767416621f * l - 3.3077115913f * m + 0.2309699292f * s,
		-1.2684380046f * l + 2.6097574011f * m - 0.3413193965f * s,
		-0.0041960863f * l - 0.7034186147f * m + 1.7076147010f * s
    );
}

vec3 hash( vec4 p )
{
    // p -= fract(p);
    vec3 q = vec3( dot(p,vec4(127.1,311.7,374.1,277.1)), 
				   dot(p,vec4(269.5,183.3,774.1,699.9)), 
				   dot(p,vec4(419.2,371.9,549.4,780.3)) );
	return fract(sin(q)*43758.5453);
}

uniform vec3 dark = vec3(0.4847, 0.025, 211.15);
uniform vec3 mid = vec3(0.6522, 0.069, 214.47);
uniform vec3 light = vec3(0.7843, 0.068, 211.52);
uniform vec3 white = vec3(0.9561, 0.025, 218.79);

uniform vec3 dark_r = vec3(79.0, 99.0, 103.0) / 255.0;
uniform vec3 mid_r = vec3(91.0, 155.0, 170.0) / 255.0;
uniform vec3 light_r = vec3(132.0, 197.0, 210.0) / 255.0;
uniform vec3 white_r = vec3(223.0, 245.0, 252.0) / 255.0;

float leng(vec3 a, vec3 b) {
    return length(a.xy - b.xy);
}

vec4 recolor( vec4 orig, vec4 rng )
{
    vec3 lab = linear_srgb_to_oklab(orig.rgb);
    float c = sqrt(lab.y * lab.y + lab.z * lab.z);
    float h = atan(lab.z, lab.y);
    vec3 lch = vec3(lab.x, c, h);
    lch += 0.2 * hash(rng) - vec3(0.2, 0.1, 0.1);

    float len = leng(lch, dark);
    vec3 col = dark_r;

    float len2 = leng(lch, mid);
    if (len2 < len) {
        len = len2;
        col = mid_r;
    }

    len2 = leng(lch, light);
    if (len2 < len) {
        len = len2;
        col = light_r;
    }

    len2 = leng(lch, white);
    if (len2 < len) {
        len = len2;
        col = white_r;
    }

    float a = col.y * cos(col.z);
    float b = col.y * sin(col.z);

    return vec4(col, orig.a);
}

vec4 effect( vec4 colour, Image texture, vec2 texture_coords, vec2 screen_coords )
{
    vec4 tex = Texel(texture, texture_coords);
    vec2 uv = (((texture_coords)*(image_details)) - texture_details.xy*texture_details.ba)/texture_details.ba;
    
    float fps = 6.0;
    vec4 seed = vec4(texture_coords * image_details, dum_Celestial.xy * fps);
    seed -= fract(seed);
    seed /= vec4(image_details, fps, fps);

    vec4 col = recolor(tex, seed);

    return dissolve_mask(col, texture_coords, uv);
}

